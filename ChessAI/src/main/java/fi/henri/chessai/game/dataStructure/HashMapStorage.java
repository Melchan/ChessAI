package fi.henri.chessai.game.dataStructure;

/**
 *
 * @author manhenri
 */
public class HashMapStorage <K, V> {
    private K key;
    private V value;
    
    public HashMapStorage(K key, V value) {
        this.key = key;
        this.value = value;
    }
    
    public K getKey() {
        return key;
    }
    
    public V getValue() {
        return value;
    }
}
